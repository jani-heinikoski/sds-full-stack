const os = require("os");

// Platform
console.log(os.platform());

// CPU arch
console.log(os.arch());

// CPU core info
console.log(os.cpus());

// Free mem
console.log(os.freemem());

// Total mem
console.log(os.totalmem());

// Home dir
console.log(os.homedir());

// Uptime
console.log(os.uptime());
